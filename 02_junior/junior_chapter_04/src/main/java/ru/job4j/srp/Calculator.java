package ru.job4j.srp;

class Calculator {

    static double add(double first, double second) {
        return first + second;
    }

    static double div(double first, double second) {
        return first / second;
    }

    static double multiply(double first, double second) {
        return first * second;
    }

    static double subtrack(double first, double second) {
        return first - second;
    }
}
